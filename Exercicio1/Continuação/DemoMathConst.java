package Exercicio1.Continuação;

public class DemoMathConst {
    public static void main(String[] args) {
        // Exemplo de uso das constantes matemáticas
        System.out.println("Constante PI: " + Math.PI);
        System.out.println("Constante E: " + Math.E);
        
        // Cálculos simples usando as constantes
        double circleArea = Math.PI * Math.pow(5, 2); // Área de um círculo com raio 5
        System.out.println("Área do círculo com raio 5: " + circleArea);
        
        double exponentialValue = Math.exp(1); // e^1
        System.out.println("Valor de e^1: " + exponentialValue);
    }
    
}
